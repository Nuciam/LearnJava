package org.example.Lec02;

public class PlusPlusMinusMinus {
    public static void main(String[] args) {

        // ************** ++; --; +=; -= ************
        int i = 5;
        int j = i++; // 5
        int k = ++i; // 6
        System.out.println(i); // 7

        int lukabula = 5;
        lukabula+=5;// lukabula = lukabula + 5;
        lukabula-=5; // lukabula = lukabula - 5;
        System.out.println(lukabula);
    }
}
